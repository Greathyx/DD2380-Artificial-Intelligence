public class Node {
    private int i;
    private int j;
    private int name;

    Node(int i, int j, int name) {
        this.i = i;
        this.j = j;
        this.name = name;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getName() {
        return name;
    }
}
